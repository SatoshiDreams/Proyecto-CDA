    private void btn_buscar_totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscar_totalActionPerformed

        try{
            int año = j_year_total.getYear();

            List<Diagnosticos> diagnosticos = diagnosticosCtx.todosDiagnosticosPorAñoConClientes(año);

            modeloTotal.setRowCount(0);
            for (int i = 0; i < diagnosticos.size(); i++) {
                modeloTotal.addRow(new Object[] {
                    diagnosticos.get(i).getFkEmpleadoId().getNombre(),
                    diagnosticos.get(i).getFkVehiculoId().getFkClienteId().getCredencial(),
                    diagnosticos.get(i).getFkVehiculoId().getFkClienteId().getNombre(),
                    diagnosticos.get(i).getFkVehiculoId().getFkClienteId().getTelefono(),
                    diagnosticos.get(i).getFkVehiculoId().getMatricula(),
                    diagnosticos.get(i).getFkVehiculoId().getTipo(),
                    new SimpleDateFormat("yyyy-MM-dd").format(diagnosticos.get(i).getFecha()),
                    diagnosticos.get(i).getResultado(),
                    diagnosticos.get(i).getObservaciones()
                });
            }
        }catch(Exception e){
            mostrarMensajeError(e);
        }
    }//GEN-LAST:event_btn_buscar_totalActionPerformed

