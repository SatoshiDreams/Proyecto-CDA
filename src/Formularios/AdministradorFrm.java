    private void btn_eliminar_empleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_empleadoActionPerformed
        try{
            int filaSeleccionada = tb_empleados.getSelectedRow();
        
            if (filaSeleccionada == -1) throw new Exception("Seleccione una fila. Por Favor.");

            String rol = (String) tb_empleados.getValueAt(filaSeleccionada, 3);

            if (!rol.equals("ADMINISTRADOR")) throw new Exception("Solo se pueden eliminar cuentas de administrador.");

            String credencial = (String) tb_empleados.getValueAt(filaSeleccionada, 0);

            empleadosCtx.eliminarEmpleadoPorCredencial(credencial);

            modeloEmpleados.removeRow(filaSeleccionada);

            JOptionPane.showMessageDialog(rootPane, "Empleado eliminado correctamente.");

            obtenerTodosLosEmpleados();
        }catch(Exception e){
            mostrarMensajeError(e);
        }
    }//GEN-LAST:event_btn_eliminar_empleadoActionPerformed

