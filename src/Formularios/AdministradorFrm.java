    public void inicializarPromediosDeTiempo() {
        try {
            List<TiempoPromedio> promedios = diagnosticosCtx.todosPromediosDeTiempo();

            modeloTiempo.setRowCount(0);
            for (int i = 0; i < promedios.size(); i++) {
                modeloTiempo.addRow(new Object[] {
                    promedios.get(i).getTipo(),
                    Math.round(promedios.get(i).getMinutos()),
                });
            }

        } catch (Exception e) {
            mostrarMensajeError(e);
        }
    }
    
    public void obtenerTodosLosEmpleados() {
        try {
            List<Empleados> empleados = empleadosCtx.obtenerTodosLosEmpleados();

            modeloEmpleados.setRowCount(0);
            for (int i = 0; i < empleados.size(); i++) {
                modeloEmpleados.addRow(new Object[] {
                    empleados.get(i).getDocumento(),
                    empleados.get(i).getNombre(),
                    empleados.get(i).getEstaActivo() ? "Desbloqueado" : "Bloqueado",
                    empleados.get(i).getFkRolId().getRol()
                });
            }

        } catch (Exception e) {
            mostrarMensajeError(e);
        }
    }
    
    private void mostrarMensajeError(Exception e) {
        JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    public void bloquearDesbloquearEmpleado() throws Exception {
        int filaSeleccionada = tb_empleados.getSelectedRow();

        if (filaSeleccionada == -1) throw new Exception("Seleccione una fila. Por Favor.");

        String credencial = (String) tb_empleados.getValueAt(filaSeleccionada, 0);

        empleadosCtx.actualizarEstadoEmpleado(credencial);

        JOptionPane.showMessageDialog(rootPane, "Empleado actualizado correctamente.");

        obtenerTodosLosEmpleados();
    }

