    private void validarCampos(String credencial, String nombre, String telefono) throws Exception {
        if (credencial.isEmpty() || nombre.isEmpty() || telefono.isEmpty()) {
            throw new Exception("Todos los campos deben ser completados.");
        }
    }
    
    private Clientes crearCliente(String credencial, String nombre, String telefono) {
        Clientes cliente = new Clientes();
        cliente.setId(UUID.randomUUID().toString());
        cliente.setCredencial(credencial);
        cliente.setNombre(nombre);
        cliente.setTelefono(telefono);
        return cliente;
    }

    private void limpiarCampos() {
        txt_credencial.setText("");
        txt_nombre.setText("");
        txt_telefono.setText("");
    }

