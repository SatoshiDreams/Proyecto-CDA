    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        try {
            String credencial = txt_credencial.getText().trim();
            String nombre = txt_nombre.getText().trim();
            String telefono = txt_telefono.getText().trim();

            validarCampos(credencial, nombre, telefono);

            if (clienteCtx.obtenerPorCredencial(credencial) != null) {
                throw new Exception("Este cliente ya está registrado.");
            }

            Clientes nuevoCliente = crearCliente(credencial, nombre, telefono);

            clienteCtx.guardar(nuevoCliente);

            limpiarCampos();

            mostrarMensaje("Cliente creado correctamente.");

        } catch (Exception e) {
            mostrarMensajeError(e);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

