    private void validarCampos(String credential, String name) throws Exception {
        if (credential.isEmpty() || name.isEmpty()) {
            throw new Exception("Rellene todos los campos.");
        }
    }

    private void verificarUsuarioExistente(Empleados user) throws Exception {
        if (user != null) {
            throw new Exception("Ya existe este mecánico.");
        }
    }

    private void verificarRolExistente(Rol role) throws Exception {
        if (role == null) {
            throw new Exception("Lo siento, hubo un error. Intenta de nuevo.");
        }
    }
    
    private Empleados crearEmpleado(String credential, String name, Rol role) {
        Empleados employee = new Empleados();
        employee.setId(UUID.randomUUID().toString());
        employee.setDocumento(credential);
        employee.setNombre(name);
        employee.setFkRolId(role);
        employee.setContrasea(BCrypt.hashpw(credential.substring(0, 4) + "*", BCrypt.gensalt()));
        employee.setEstaActivo(true);
        return employee;
    }

    private void limpiarCampos() {
        txt_credencial.setText("");
        txt_nombre.setText("");
    }

