    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        try {
            String credential = txt_credencial.getText();
            String name = txt_nombre.getText();
            String rol = (String) cbx_roles.getSelectedItem();

            validarCampos(credential, name);

            Empleados user = empleadosCtx.obtenerPorCredencial(credential);
            verificarUsuarioExistente(user);

            Rol role = rolesCtx.obtenerPorRol(rol);
            verificarRolExistente(role);

            Empleados employee = crearEmpleado(credential, name, role);

            empleadosCtx.guardar(employee);

            limpiarCampos();

            mostrarMensaje("Rol creado correctamente.");

        } catch (Exception e) {
            mostrarMensajeError(e);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

